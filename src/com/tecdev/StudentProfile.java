    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String en = txtEnrollment.getText().trim(); //Page 5
        String n = txtName.getText().trim(); //Remove Leading/Trailing spc
        String q = cmbQualification.getSelectedItem().toString();
        String w = txtWeightage.getText();
        String b = cmbBranch.getSelectedItem().toString();
        Date d = dtpDob.getDate();  //RC=>Fix Imports [ Ctrl Shift i ]
        String g = cmbGender.getSelectedItem().toString();
        String m = txtMobile.getText().trim();
        String l = cmbLanguage.getSelectedItem().toString();
        String em = txtEmail.getText().trim().toLowerCase(); //OCJP Chaining
        String desc = txtDescription.getText().trim();
        if (en.length() == 0 || n.length() == 0 || m.length() == 0 || em.length() == 0 || desc.length() == 0) {
            JOptionPane.showMessageDialog(null, "All Fields are Compulsory");
            return;
        }
        if (!en.matches("^[0-9]{4}(CS|IT|EC|CE|EX)[0-9]{2}1[0-9]{3}$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Enrollment Number");
            return;
        }
        if (!em.matches("^(.+)@(.+)$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Email");
            return;
        }
        if (!m.matches("^[1-9][0-9]{9}$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Mobile");
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yy");
        String dob = sdf.format(d);  //Format d as per sdf
        String operation = btnEdit.getText();
        String sql = "";
        if (operation.equals("Add")) {
            sql = String.format("Insert into Student Values('%s' , '%s' ,'%s' , '%s' , '%s' , '%s' , '%s' , '%s' , '%s' , '%s' ,'%s' ,'%s')", userid, en, n, q, w, b, dob, g, em, l, desc,m);
        } else if (operation.equals("Edit")) {
            sql = String.format("Update Student set name='%s',qualification='%s',weightage='%s',branch='%s',mobile='%s',email='%s',language='%s',description='%s' where userid='%s'", n, q, w, b, m, em, l, desc,userid);
        }
        //JOptionPane.showMessageDialog(null,  sql);

        try {
            int result = DBConnection.executeUpdate(sql);
            if (result > 0) {
                DBConnection.commit();
                JOptionPane.showMessageDialog(null, "Profile Updated");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Save");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            editable(false);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

