    private void initialize() throws SQLException {
        String sql = String.format("Select * from Student where userid='%s' ", userid);
        ResultSet rs = DBConnection.executeQuery(sql);
        if (rs.next()) {
            rsToText(rs);
        } else {
            btnEdit.setText("Add");
        }

    }

