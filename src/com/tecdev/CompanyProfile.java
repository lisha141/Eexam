    private void initialize() throws SQLException {
        String sql = String.format("Select * from company where userid='%s'", userid);
        ResultSet rs = DBConnection.executeQuery(sql);
        if (rs.next()) {  //gives true when row found
            rsToText(rs);  //Display data into TextField
        } else {
            btnEdit.setText("Add");
        }
    }

    //Definition of UDF
