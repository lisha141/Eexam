    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
String cn = txtCname.getText().trim();
        String ce = txtCEO.getText().trim(); //Remove Leading/Trailing spc
        Date d = dtpEstd.getDate();
        String ad = txtAddress.getText();
        String w=txtWebsite.getText().trim();
        String f = cmbField.getSelectedItem().toString();
        String p1 = txtPhone1.getText().trim();
        String p2 = txtPhone2.getText().trim();
        String em = txtEmail.getText().trim().toLowerCase(); //OCJP Chaining
        String ab = txtAbout.getText().trim();
        if (cn.length() == 0 || ce.length() == 0 || ad.length() == 0 || w.length() == 0 || p1.length() == 0 || em.length()==0 || ab.length()==0) {
      JOptionPane.showMessageDialog(null, "Fields marked with * are Compulsory");
            return;
        }
        if (ce.matches("^[A-Z][a-z ]+$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid CEO Name");
            return;
        }
        if (!p1.matches("^[1-9]{3}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Phone Number");
            return;
        }
        if (!p2.matches("^[1-9]{3}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Phone Number");
            return;
        }
        if (!em.matches("^(.+)@(.+)$")) {
            JOptionPane.showMessageDialog(null, "Sorry!! Invalid Email");
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yy");
        String estd = sdf.format(d);  //Format d as per sdf
        String operation = btnEdit.getText();
        String sql = "";
        if (operation.equals("Add"))
            sql = String.format("Insert into Company Values('%s' , '%s' , '%s' , '%s' ,'%s' , '%s' , '%s' , '%s' , '%s' , '%s' , '%s')", userid, cn, ad,f, w, p1, p2, em, ab, ce, estd);
        else if (operation.equals("Edit"))
            sql = String.format("Update Company set cname='%s', address='%s' , field='%s', website='%s' ,phone1='%s' , phone2='%s' ,email='%s' , about='%s', ceo='%s' where userid='%s'", cn, ad, f, w, p1,p2, em,ab,ce, userid);
            JOptionPane.showMessageDialog(null, sql);
           try {
                int result = DBConnection.executeUpdate(sql);
                if (result > 0) {
                    DBConnection.commit();
                    btnEdit.setText("Edit");
                    JOptionPane.showMessageDialog(null, "Profile Updated");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            finally {
                editable(false);
}
                    // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

