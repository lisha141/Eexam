    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String jobid = txtJobid.getText().trim();
        if (jobid.length() == 0) {
            JOptionPane.showMessageDialog(null, "No Job to Delete");
            return;
        }
        int choice = JOptionPane.showConfirmDialog(this,
                "Are you Sure?", "Confirm?",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (choice == JOptionPane.NO_OPTION) {
            return;
        }
        sql = String.format("Delete From PostJob Where jobid='%s' ", jobid);
        try {
            int result = DBConnection.executeUpdate(sql);
            if (result > 0) {
                DBConnection.commit();
                JOptionPane.showMessageDialog(null, "Job Deleted");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Delete " + e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

