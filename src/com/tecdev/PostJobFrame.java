    private void initialize() throws SQLException {
        try {
            sql = String.format("Select * from PostJob Where userid='%s' ", userid);
            rs1 = DBConnection.executeQuery(sql);
            if (rs1.last()) {
                rsToText(rs1);
            } else {
                
                emptyText(); //UDF
            }
            fetchCompanyName();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

    private void fetchCompanyName() throws SQLException {
        sql = String.format("select cname from company where userid='%s' ", userid);
        ResultSet rs = DBConnection.executeQuery(sql);
        if (rs.next()) { //Fetch Row if found
            String cname = rs.getString("cname");
            txtCname.setText(cname);
            JOptionPane.showMessageDialog(null, "Cname=" + cname);
        }
    }

