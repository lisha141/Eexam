    private void navigationClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_navigationClick
        if (findPressed) {

            try {
                initialize();
            } catch (SQLException ex) {
                Logger.getLogger(PostJobFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            findPressed = false;
        }
        try {
            switch (evt.getActionCommand()) {
                case "First":
                    if (!rs1.first()) {
                        rs1.first();
                    }
                    break;
                case "Previous":
                    rs1.previous();
                    if (rs1.isBeforeFirst()) {
                        rs1.last();
                    }
                    break;
                case "Next":
                    rs1.next();
                    if (rs1.isAfterLast()) {
                        rs1.first();
                    }
                    break;
                case "Last":
                    if (!rs1.next()) {
                        rs1.last();
                    }
                    break;
            }
            rsToText(rs1);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_navigationClick

