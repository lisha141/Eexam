    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        // TODO add your handling code here:
      int count=jTable1.getRowCount();
        if(count==0)        {
            JOptionPane.showMessageDialog(null, "Sorry. No Row Found");
            return;
        }
        StringBuffer sb=new StringBuffer(1024);
        sb.append("<html><head>Jobs</head><body>");
        sb.append("<table border=1 width=100%>");    
        for(int i=0; i<count; i++)
        {
        sb.append("<tr>");
        sb.append("<td>" + jTable1.getValueAt(i, 0)); // i-th row, 1 col
        sb.append("<td>" + jTable1.getValueAt(i, 1)); // i-th row, 2 col
        sb.append("</tr>");
        }
        sb.append("</table>");
        sb.append("</body></html>");
        try{
        FileOutputStream fos=new FileOutputStream("d:/" + userid+ ".html");
        fos.write(sb.toString().getBytes());
        fos.close();
        jTextPane1.setText(sb.toString());
         JOptionPane.showMessageDialog(null, "File Saved Successfully as d:/" + userid + ".html");
        }
        catch(Exception e)        {
            JOptionPane.showMessageDialog(null, e);
        }
    


        
          
    }//GEN-LAST:event_btnSaveActionPerformed

