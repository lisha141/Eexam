    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        int index=jTable1.getSelectedRow();
        if(index==-1){
            JOptionPane.showMessageDialog(null,"Please Select Post To Apply!");
            
        }
        int rows[]=jTable1.getSelectedRows();
        int result=0,count=0;
        String jobid,weightage,sql="";
        try{
            for(int r:rows){
                jobid=jTable1.getValueAt(r,1).toString();
                weightage=jTable1.getValueAt(r,3).toString();
               sql=String.format("Insert into Applied Values('%s','%s',sysdate,'%s','false',appid_seq.nextval)",userid,jobid,weightage);
               result=DBConnection.executeUpdate(sql);
               if(result>0){
                   count++;
               }
               if(count>0){
                   DBConnection.commit();
                   JOptionPane.showMessageDialog(null,"You have applied for"+count+"Jobs");
                   initialize();
               }else
                   JOptionPane.showMessageDialog(null,"Failed to Apply for jobs!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnApplyActionPerformed

